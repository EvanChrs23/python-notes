weather = input("What is the current weather?: ")

if weather == "sunny":
    print("sunglasses and light clothes")
elif weather == "rainy":
    print("an umbrella and waterproof shoes")
elif weather == "snowy":
    print("a thick coat, gloves, and a scarf")
elif weather == "windy":
    print("a windbreaker jacket")
elif weather == "cold":
    print("a sweater and a jacket")
else:
    print("Sorry, I don't have advice for that weather.")